    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        try {
            stmt = conn.createStatement();

            String stuName = name.getText();
            String gender = sGender.getText();
            String stuGuardianName = fName.getText();
            String stuClass = sClass.getText();
            String blood = (String) sBlood.getSelectedItem();
            String city = sCity.getText();
            String contactNumber = sConNumber.getText();

            if (stuName.equals("") || gender.equals("") || stuGuardianName.equals("") || stuClass.equals("")
                    || city.equals("")) {
                JOptionPane.showMessageDialog(null, "Cannot Insert empty values.", "Validation Error",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                String sql = "INSERT INTO STUDENT(stuName, gender, stuFatherName, stuClass, blood, city, contactNumber) VALUES('"
                        + stuName + "' , '" + gender + "', '" + stuGuardianName + "', '" + stuClass + "', '" + blood
                        + "', '" + city + "', '" + contactNumber + "') ";
                stmt.execute(sql);

                JOptionPane.showMessageDialog(null, "New student account created Successfully.", "Query Success",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Check for the database connection.", "Database Error",
                    JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Try again Later in some times.", "Server Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }// GEN-LAST:event_jButton1ActionPerformed

